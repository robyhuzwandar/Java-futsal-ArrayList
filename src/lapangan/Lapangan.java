public class Lapangan {

    private int id;
    private String nmLapangan;
    private String jenisLapangan;

    public String getNmLapangan() {
        return nmLapangan;
    }

    public void setNmLapangan(String nmLapangan) {
        this.nmLapangan = nmLapangan;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getJenisLapangan() {
        return jenisLapangan;
    }

    public void setJenisLapangan(String jenisLapangan) {
        this.jenisLapangan = jenisLapangan;
    }

}
